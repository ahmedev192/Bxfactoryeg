const NON_JOINING = new Set([0x0621, 0x0622, 0x0623, 0x0624, 0x0625, 0x0627, 0x0629, 0x062f, 0x0630, 0x0631, 0x0632, 0x0648, 0x0649]);

export function reshapeArabic(text: string): string {
  const chars = [...text];
  const n = chars.length;
  const out: string[] = [];
  for (let i = 0; i < n; i++) {
    const code = chars[i].codePointAt(0)!;
    const forms = FORMS[code];
    if (!forms) {
      out.push(chars[i]);
      continue;
    }
    const prevCode = i > 0 ? chars[i - 1].codePointAt(0)! : 0;
    const nextCode = i < n - 1 ? chars[i + 1].codePointAt(0)! : 0;
    const prevJoins = prevCode in FORMS && !NON_JOINING.has(prevCode) && FORMS[prevCode][2];
    const nextJoins = nextCode in FORMS;
    let form: number;
    if (prevJoins && nextJoins && forms[3]) form = forms[3];
    else if (prevJoins && forms[1]) form = forms[1];
    else if (nextJoins && forms[2]) form = forms[2];
    else form = forms[0];
    out.push(form ? String.fromCodePoint(form) : chars[i]);
  }
  return out
    .join('')
    .split(' ')
    .map((w) => [...w].reverse().join(''))
    .reverse()
    .join(' ');
}

export function ar(text: string): string {
  if (!text) return '';
  if (!/[\u0600-\u06FF]/.test(text)) return text;
  return reshapeArabic(text);
}
